#  Numeric Types
A = 42
print(type(A))        

F = 3.14
print(type(F))      

com = 1 + 2j
print(type(com))    

# Sequence Types
S = "Hello"
print(type(S))      

L = [1, 2, 3]
print(type(L))    

T = (1, 2, 3)
print(type(T))      

R = range(5)
print(type(R))      

# Mapping Type
D = {"a": 1, "b": 2}
print(type(D))      

# Set Types
s = {1, 2, 3}
print(type(s))        

FS= frozenset({1, 2, 3})
print(type(FS))  

# Boolean Type
B = True
print(type(B))       

# None Type
N = None
print(type(N))       
